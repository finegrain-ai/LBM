def get_aspect_ratios(target_resolution: int) -> dict[str, tuple[int, int]]:
    if target_resolution == 4096:
        return ASPECT_RATIO_4096
    elif target_resolution == 2880:
        return ASPECT_RATIO_2880
    elif target_resolution == 2048:
        return ASPECT_RATIO_2048
    elif target_resolution == 1024:
        return ASPECT_RATIO_1024
    elif target_resolution == 512:
        return ASPECT_RATIO_512
    elif target_resolution == 256:
        return ASPECT_RATIO_256
    else:
        raise ValueError(f"Unsupported image size: {target_resolution}")

def get_closest_ratio(height: int, width: int, aspect_ratios: dict[str, tuple[int, int]]) -> str:
    ratio = height / width
    return min(aspect_ratios.keys(), key=lambda r: abs(float(r) - ratio))

def get_target_size(
    target_resolution:int, 
    height: int, 
    width: int
) -> tuple[int, int]:
    aspect_ratios = get_aspect_ratios(target_resolution)
    closest_ratio = get_closest_ratio(height=height, width=width, aspect_ratios=aspect_ratios)
    return aspect_ratios[closest_ratio]